function fillLocale(localeKey, translations) {
  const filePath = path.join(LOCALES, `${localeKey}.ts`);
  let content = fs.readFileSync(filePath, "utf-8");

  // Remove the outer export/default boilerplate first to just get the dict
  const startMatch = content.match(/const \w+: Record<string, string> = ({[\s\S]*?\n});/);
  if (!startMatch) {
    console.error(`Could not parse ${localeKey}.ts`);
    return;
  }

  let existingDict = {};
  const parseFn = new Function("Record", "string", `return ${startMatch[1]}`);
  try { existingDict = parseFn({}, ""); } catch (e) { console.error(`Parse error in ${localeKey}.ts:`, e.message); return; }

  const existingKeys = Object.keys(existingDict);
  const missingKeys = enKeys.filter(k => !existingKeys.includes(k) && translations[k]);

  if (missingKeys.length === 0) {
    console.log(`  ${localeKey}: ✅ already complete (${existingKeys.length} keys)`);
    return;
  }

  // Build the new dictionary entry
  let newEntries = "";
  for (const key of enKeys) {
    const val = existingDict[key] || translations[key];
    if (val) {
      // Escape double quotes and backslashes
      const escaped = val.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
      newEntries += `  "${key}": "${escaped}",\n`;
    }
  }

  // Reconstruct the file
  const exportName = localeKey; // nl, pl, ja, etc.
  const lines = content.split("\n");
  // Find the closing }; that ends the dictionary
  let dictStart = -1, dictEnd = -1;
  for (let i = 0; i < lines.length; i++) {
    if (lines[i].includes(`const ${exportName}: Record<string, string> = {`)) dictStart = i;
    if (dictStart > 0 && lines[i].trim() === "};") { dictEnd = i; break; }
  }

  if (dictStart < 0 || dictEnd < 0) {
    console.error(`  ${localeKey}: could not find dict boundaries`);
    return;
  }

  const header = lines.slice(0, dictStart + 1).join("\n");
  const footer = lines.slice(dictEnd).join("\n");
  const newContent = header + "\n" + newEntries + "};\n" + footer;

  fs.writeFileSync(filePath, newContent);
  console.log(`  ${localeKey}: filled ${missingKeys.length} missing keys (total: ${Object.keys(translations).length})`);
}

// Process each locale
for (const [locale, dict] of Object.entries(TRANSLATIONS)) {
  fillLocale(locale, dict);
}

